    <h2>EP Explained</h2>
    <br />
    Each Blood and Fury member now has an officer note that has two numbers. 
    The first number is your EP. EP stands for effort points. You earn effort points for raiding and doing certain other things that are cool. 
    For now, EP will be earned for killing raid bosses in, guild bank donations(check the g-bank info for more explaination) and spending time on progression bosses. 
    Double EP for guild first boss kills! If somebody is asked to sit out of a raid because it is full, or we need to bring a specific class - don’t worry! 
    You still earn full EP on the waitlist :)
