    <br />
    <h2>GP Explained</h2>
    <br />
    The second number is your GP. GP stands for gear points. When you get a piece of loot, your GP goes up. 
    The more hype the loot, the more GP you get. If you want to see the GP cost of items, download the addons posted in the discord channel, 
    and the GP value will show in item tooltips in game. Better items and more important items cost more GP.
    <br />
    <br />
    <h2>Loot Distribution</h2>
    <br />
    So who gets the loot? Your loot rating is your EP divided by your GP. EP/GP… get it? 
    When multiple people bid on an item, the player with the highest EP/GP rating gets it. 
    So when you do cool stuff like raiding, you get EP and your loot rating goes up. When you get a shiny new item, you gain GP and your loot rating goes down. 
    It’s a bit like suicide kings in that sense - go to raids and you move up, get loot and you move down. Everybody starts with 200 GP.
    <br /> 
    That’s all there is to it… except…
    <br />
    <br />
    <h2>Decay</h2>
    <br />
    Decay. Each week players lose 10% of their EP, but also 10% of their GP. 
    There are a number of reasons for this, but mainly it means that things that happened recently will matter a bit more than things that happened months ago 
    - and it prevents numbers from getting way too high. Basically, each week everybody earns a little refund on the loot you’ve gotten, 
    and everybody loses a bit of EP along with it.
    <br />
    <br />
    <h2>Closing Thoughts</h2>
    <br />
    How you use your priority rating is up to you… but the decay means that a player who bids on lots of stuff will get slightly more loot in the long run compared to a player who saves up so they can be the top bidder when a really super-hype item drops. 
    Either approach works, it all depends on your personal style! MS>OS and OS items have half price if no one needs the item for their mainspec


